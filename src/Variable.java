public class Variable {
    public static void main(String[] args) {
        int count;
        count = 10;
        System.out.println(count);

        double avg = 11.123;
        System.out.println(avg);

        String name = "Caramel";
        System.out.println(name);

        int totalCount;
        totalCount = 123;
        System.out.println(totalCount);
    }
}
