public class MathExam {
    public static void main(String[] args) {
        int i = Math.max(5,10);
        System.out.println(i);
        int j = Math.min(2,4);
        System.out.println(j);
        int k = Math.abs(-10);
        System.out.println(k);
        System.out.println(Math.random());
        System.out.println(Math.sqrt(25.1));

    }
}
